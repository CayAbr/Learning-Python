#Tuples

my_tuple = (1, 2, 3, 4, 5)

print(my_tuple)
print(my_tuple[0])  # Accesses the first element in the tuple
print(my_tuple[2])  # Accesses the third element in the tuple
print(my_tuple[-1])  # Accesses the last element in the tuple

tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)

conc_tuple = tuple1 + tuple2  # Concatenates two tuples

print(conc_tuple)  # Prints the concatenated tuple

rep_tuple = tuple1 * 3 # Repeats tuple1 three times

print(rep_tuple)  # Prints the repeated tuple